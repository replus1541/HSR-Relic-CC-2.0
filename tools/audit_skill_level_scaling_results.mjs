import fs from "node:fs";

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, "utf8"));
}

function pct(value) {
  if (typeof value !== "number" || !Number.isFinite(value)) return "";
  return `${Math.round(value * 10000) / 100}%`;
}

function compactText(value) {
  return String(value ?? "").replace(/\s+/g, " ").trim();
}

const effects = readJson("data/generated/effect-rows.json").rows;
const resolved = readJson("data/generated/resolved-effects.json").rows;
const resolvedByEffectRowId = new Map(resolved.map((row) => [row.effectRowId, row]));

const rows = effects
  .filter((effect) => effect.skillScaling)
  .map((effect) => {
    const result = resolvedByEffectRowId.get(effect.id);
    return {
      effectRowId: effect.id,
      characterId: effect.effectProviderId,
      characterName: effect.characterName,
      stat: effect.stat,
      sourceTrace: effect.sourceTrace,
      sourceText: compactText(effect.sourceText),
      sourceLevel1Value: effect.rawValue,
      sourceLevel1ValuePct: pct(effect.rawValue),
      valueMode: effect.valueMode,
      calculationStatus: result?.calculationStatus ?? effect.calculationStatus,
      blockedReason: result?.blockedReason ?? effect.blockedReason ?? null,
      resolvedValue: result?.resolvedValue ?? null,
      resolvedValuePct: pct(result?.resolvedValue),
      effectiveLevel: result?.valueTrace?.effectiveLevel ?? null,
      baseLevel: effect.skillScaling.baseLevel ?? null,
      eidolonLevelBonuses: effect.skillScaling.eidolonLevelBonuses ?? [],
      skillTitle: effect.skillScaling.skillTitle ?? null,
      skillCategory: effect.skillScaling.skillCategory ?? null,
      coefficientLabel: effect.skillScaling.coefficientLabel ?? null,
      coefficientValues: effect.skillScaling.coefficientValues ?? [],
      matchType: effect.skillScaling.matchType ?? null,
      missingReason: effect.skillScaling.missingReason ?? null,
    };
  });

const counts = rows.reduce((acc, row) => {
  const key = row.calculationStatus === "calculation_ready" ? "resolved" : `blocked:${row.blockedReason ?? "unknown"}`;
  acc[key] = (acc[key] ?? 0) + 1;
  return acc;
}, {});

const output = {
  version: 1,
  generatedBy: "tools/audit_skill_level_scaling_results.mjs",
  policy: {
    baseLevels: {
      basicAttack: 6,
      combatSkill: 10,
      ultimate: 10,
      talent: 10,
    },
    eidolonHandling: "E3/E5 level bonuses from character eidolon text are applied, then clamped to the hard cap. Lv15 is a system cap, not the default calculation level.",
    missingTableHandling: "Primary leveled ability numeric effects without a matched coefficient row are blocked instead of using the level-1 description value.",
  },
  totalRows: rows.length,
  counts,
  rows,
};

fs.writeFileSync("reports/extraction/skill-level-scaling-results.json", `${JSON.stringify(output, null, 2)}\n`, "utf8");

const lines = [
  "# Skill Level Scaling Results",
  "",
  "Generated by `node tools/audit_skill_level_scaling_results.mjs` after adapter, normalization, and value-resolution generation.",
  "",
  "## Policy",
  "",
  "- Lv15 is only the hard system cap. It is not used as the default max-damage level.",
  "- Default max levels are basic attack Lv6 and combat skill / ultimate / talent Lv10.",
  "- E3/E5 level bonuses are parsed per character and per skill category, then applied to the default max level.",
  "- If a primary leveled ability has no matched coefficient row, the effect is blocked instead of using the level-1 HoyoWiki description value.",
  "",
  "## Summary",
  "",
  `- total skill-level audited rows: ${rows.length}`,
  ...Object.entries(counts).map(([key, value]) => `- ${key}: ${value}`),
  "",
  "## Resolved Rows",
  "",
  ...rows.filter((row) => row.calculationStatus === "calculation_ready").map((row) => (
    `- ${row.characterName} (${row.characterId}) / ${row.stat}: ${row.sourceLevel1ValuePct} -> ${row.resolvedValuePct} at Lv${row.effectiveLevel}; ${row.skillTitle} / ${row.coefficientLabel}`
  )),
  "",
  "## Blocked Missing Tables",
  "",
  ...rows.filter((row) => row.calculationStatus !== "calculation_ready").map((row) => (
    `- ${row.characterName} (${row.characterId}) / ${row.stat}: source Lv1 ${row.sourceLevel1ValuePct}; ${row.skillTitle}; reason=${row.missingReason ?? row.blockedReason}; source=${row.sourceTrace}`
  )),
  "",
];

fs.writeFileSync("reports/extraction/skill-level-scaling-results.md", `${lines.join("\n")}\n`, "utf8");

console.log(JSON.stringify({
  totalRows: rows.length,
  counts,
  report: "reports/extraction/skill-level-scaling-results.md",
}, null, 2));
